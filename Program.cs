        public static ushort derive_key(ushort seed, ushort algo) {
            int v1, v2, v3, v4;

            key = seed;
            int extended_table  = algo >> 12;
            int table_select    = (byte)(algo >> 8) & 0xF;
            int pos             = 13 * (byte)algo;
            byte opcode         = 0;
            int counter         = 4;
            ushort data         = 0;

            // select correct security table to use
            while (counter > 0) {
                if (table_select != 3) {
                    if (table_select != 4) {
                        // table 1
                        data = (ushort)((table1[pos + 2] << 8) | table1[pos + 3]);
                        opcode = (byte)table1[pos+1];
                    }
                    else {
                        // table 3
                        data = (ushort)((table3[pos + 2] << 8) | table3[pos + 3]);
                        opcode = (byte)table3[pos + 1];
                    }
                }
                else {
                    // table 2
                    data = (ushort)((table2[pos + 2] << 8) | table2[pos + 3]);
                    opcode = (byte)table2[pos + 1];
                }

                if (extended_table > 0) {
                    // table 3
                    data = (ushort)((table3[pos + 2] << 8) | table3[pos + 3]);
                    opcode = (byte)table3[pos + 1];
